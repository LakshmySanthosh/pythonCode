# Create some strings
string1 = "Hello"
string2 = "world!"
print("The strings are: ")
print(string1)
print(string2, "\n")

# Concatenate the strings

string3 = string1 + " " + string2

# Print the concatenated string
print("The concatenated string is: " )
print(string3, "\n")

# Access a substring
substring = string3[0:5]

# Print the substring
print("Substring of the above printed string from 0 to 5 is: ")
print(substring)